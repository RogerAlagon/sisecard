CREATE TABLE ALUMNOS
(ID INT(11)PRIMARY KEY AUTO_INCREMENT,
 CODIGO VARCHAR(8),
 NOMBRE VARCHAR(50),
 APELLIDOS VARCHAR(100),
 PROGRAMA VARCHAR(50));

 CREATE TABLE PROGRAMAS
 (PROGRAMA VARCHAR(50));

CREATE TABLE LABORATORIO
(COD_LABORATORIO VARCHAR(10) PRIMARY KEY,
NOMBRE_LABORATORIO VARCHAR(50));

CREATE TABLE PROFESOR
(COD_PROFESOR VARCHAR(10) PRIMARY KEY,
NOMBRE_PROFESOR VARCHAR(50),
APELLIDO_P_PROFESOR VARCHAR(50),
APELLIDO_M_PROFESOR VARCHAR(50));

CREATE TABLE CURSO
(COD_CURSO VARCHAR(10) PRIMARY KEY,
NOMBRE_CURSO VARCHAR(100));

CREATE TABLE LUNES
(COD_LUNES VARCHAR(10) PRIMARY KEY,
COD_CURSO VARCHAR(10),
NOMBRE_ASIGNATURA VARCHAR(100),
COD_PROFESOR VARCHAR(10),
FOREIGN KEY (COD_CURSO) REFERENCES CURSO (COD_CURSO),
FOREIGN KEY (COD_PROFESOR) REFERENCES PROFESOR (COD_PROFESOR));

CREATE TABLE MARTES
(COD_MARTES VARCHAR(10) PRIMARY KEY,
COD_CURSO VARCHAR(10),
NOMBRE_ASIGNATURA VARCHAR(100),
COD_PROFESOR VARCHAR(10),
FOREIGN KEY (COD_CURSO) REFERENCES CURSO (COD_CURSO),
FOREIGN KEY (COD_PROFESOR) REFERENCES PROFESOR (COD_PROFESOR));

CREATE TABLE MIERCOLES
(COD_MIERCOLES VARCHAR(10) PRIMARY KEY,
COD_CURSO VARCHAR(10),
NOMBRE_ASIGNATURA VARCHAR(100),
COD_PROFESOR VARCHAR(10),
FOREIGN KEY (COD_CURSO) REFERENCES CURSO (COD_CURSO),
FOREIGN KEY (COD_PROFESOR) REFERENCES PROFESOR (COD_PROFESOR));

CREATE TABLE JUEVES
(COD_JUEVES VARCHAR(10) PRIMARY KEY,
COD_CURSO VARCHAR(10),
NOMBRE_ASIGNATURA VARCHAR(100),
COD_PROFESOR VARCHAR(10),
FOREIGN KEY (COD_CURSO) REFERENCES CURSO (COD_CURSO),
FOREIGN KEY (COD_PROFESOR) REFERENCES PROFESOR (COD_PROFESOR));

CREATE TABLE VIERNES
(COD_VIERNES VARCHAR(10) PRIMARY KEY,
COD_CURSO VARCHAR(10),
NOMBRE_ASIGNATURA VARCHAR(100),
COD_PROFESOR VARCHAR(10),
FOREIGN KEY (COD_CURSO) REFERENCES CURSO (COD_CURSO),
FOREIGN KEY (COD_PROFESOR) REFERENCES PROFESOR (COD_PROFESOR));

 CREATE TABLE CURSOS
 (ID_ALUMNO VARCHAR(8),
 HORA VARCHAR(20),
 DIA VARCHAR(50),
 CURSO_DESCRIPCION VARCHAR(100),
 PROFESOR_DESCRIPCION VARCHAR(50),
 DESCRIPCION_LABORATORIO VARCHAR(20));
 

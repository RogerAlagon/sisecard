 HORARIO.INICIO,
 HORARIO.FIN,
 LUNES.NOMBRE_ASIGNATURA AS LUNES_ASIGNATURA,
 PROFESOR.NOMBRE_PROFESOR,
 MARTES.NOMBRE_ASIGNATURA AS MARTES_ASIGNATURA,
